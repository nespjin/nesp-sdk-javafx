package com.nesp.javafx.sdk.utils;

public class Log {

    public static void print(String TAG, String message) {
        System.out.println("TAG:" + TAG + "|| MSG:" + message + "\n");
    }

    public static void print(String TAG, double message) {
        System.out.println("TAG:" + TAG + "|| MSG:" + message + "\n");
    }
}
